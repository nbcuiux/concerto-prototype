    Searchfield.prototype._init = function() {
        var self = this;

        this.items = this.options.initialItems || [];

        this.searchWrapper = $('<div></div>').addClass('searchfield__wrapper');


        this.tagfieldWrapper = document.createElement('div');
        this.tagfieldWrapper.classList.add('tagfield__wrapper');
        this.el.parentNode.insertBefore(this.tagfieldWrapper, this.el);
        this.tagfieldWrapper.appendChild(this.el);

        if (this.options.label) {
            this.label = $('<div></div>').addClass('searchfield__label')
                                         .text(this.options.label)
                                         .attr('for', this.el.id);
            this.searchWrapper.append(this.label);
        }
        if (this.options.helpText) {
            this.helpText = $('<div></div>').addClass('searchfield__help-text')
                                         .text(this.options.label);
            this.searchWrapper.appendChild(this.helpText);
        }
        if (this.options.errMsg) {
            this.errMsg = $('<div></div>').addClass('searchfield__err-msg')
                                         .text(this.options.label);
            this.searchWrapper.appendChild(this.errMsg);
        }

        if (this.items.length > 0) {
            self.el.classList.add('has-values', 'js-hasValue');
            self.items.forEach(function(item) {
                self.el.appendChild(self._createTag(item));
            });
            self._toggleAddable();
        }
    };

